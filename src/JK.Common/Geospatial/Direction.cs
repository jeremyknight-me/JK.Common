namespace JK.Common.Geospatial;

/// <summary>
/// Represents the cardinal directions for geographic coordinates.
/// </summary>
public enum Direction
{
    /// <summary>
    /// North direction.
    /// </summary>
    N = 0,
    /// <summary>
    /// South direction.
    /// </summary>
    S = 1,
    /// <summary>
    /// East direction.
    /// </summary>
    E = 2,
    /// <summary>
    /// West direction.
    /// </summary>
    W = 3
}
